print "digite número de 1 a 7: ";

numero = Integer(gets());

case numero

when 1 

    puts "Domingo";

when 2

    puts "Segunda-Feira";

when 3

    puts "Terça-Feira";

when 4

    puts "Quarta-Feira";

when 5 

    puts "Quinta-Feira";

when 6

    puts "Sexta-Feira";

when 7

    puts "Sábado";

else 

    puts "Número Inválido";

end